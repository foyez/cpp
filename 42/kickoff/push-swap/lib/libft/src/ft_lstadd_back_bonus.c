#include "libft.h"

void	ft_lstadd_back(t_list **list, t_list *new)
{
	t_list	*cur;

	if (!*list)
	{
		*list = new;
		return ;
	}
	cur = *list;
	while (cur->next)
		cur = cur->next;
	cur->next = new;
}
